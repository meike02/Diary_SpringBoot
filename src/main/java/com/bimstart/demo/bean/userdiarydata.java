package com.bimstart.demo.bean;

public class userdiarydata {
   private String   eamil ;
   private String   diaryurl ;
   private boolean   overt ;
   private Integer   lovepeoples ;

    public String getEamil() {
        return eamil;
    }

    public void setEamil(String eamil) {
        this.eamil = eamil;
    }

    public String getDiaryurl() {
        return diaryurl;
    }

    public void setDiaryurl(String diaryurl) {
        this.diaryurl = diaryurl;
    }

    public boolean isOvert() {
        return overt;
    }

    public void setOvert(boolean overt) {
        this.overt = overt;
    }

    public Integer getLovepeoples() {
        return lovepeoples;
    }

    public void setLovepeoples(Integer lovepeoples) {
        this.lovepeoples = lovepeoples;
    }
}
